public class User {

  int age;

  String name;


  User(int age, String name) {
    this.age = age;
    this.name = name;
  }


  int getAge() {
    return age;
  }


  void setAge(int age) {
    this.age = age;
  }


  String getName() {
    return name;
  }

  void setName(String name) {
    this.name = name;
  }
}
